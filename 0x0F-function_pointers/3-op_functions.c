#include "3-calc.h"

/**
 * op_add - adds two ints
 *
 * @a: First int
 * @b: Second int
 * Return: sum of two nums
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substracts two ints
 *
 * @a: First int
 * @b: Second int
 * Return: difference of two nums
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two ints
 *
 * @a: First int
 * @b: Second int
 * Return: product of two nums
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two ints
 *
 * @a: First int
 * @b: Second int
 * Return: division of two nums
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus of two ints
 *
 * @a: First int
 * @b: Second int
 * Return: modulus of two nums
 */
int op_mod(int a, int b)
{
	return (a % b);
}
